import logging
import requests
import os

logger = logging.getLogger("HF_GENERATOR")
logging.basicConfig(level=logging.INFO, format="%(asctime)s | HF_GENERATOR | %(levelname)s | %(message)s")

# Read the HuggingFace API URL from environment (if set)
HF_URL = os.getenv("HF_URL", "http://127.0.0.1:8010/generate")

def generate_app_code(prompt: str) -> str:
    """
    Generate HTML app code based on a given prompt.
    If HF_URL is not reachable (like on Render), returns a mock HTML page.
    """

    logger.info(f"üß† Sending prompt to HuggingFace server: {HF_URL}")

    try:
        # Try connecting to actual HuggingFace generator
        response = requests.post(HF_URL, json={"prompt": prompt}, timeout=20)

        if response.status_code == 200:
            try:
                data = response.json()
                logger.info("‚úÖ Code generation completed successfully.")
                return data.get("code", data.get("html", "<h1>Generated: OK</h1>"))
            except ValueError:
                # Not valid JSON ‚Üí fallback to raw text
                logger.warning("‚ö†Ô∏è HF output not valid JSON, returning as single file.")
                return response.text

        else:
            logger.error(f"‚ùå HF generation failed with status {response.status_code}")
            return _mock_html(prompt)

    except Exception as e:
        # On Render, the 127.0.0.1:8010 endpoint won‚Äôt exist ‚Üí fallback safely
        logger.error(f"‚ùå HF request failed: {e}")
        return _mock_html(prompt)


